 var fix= document.getElementById("show");
 function onClick(){
    if(fix.style.display=="none"){
        fix.style.display="block";
    }
    else{
        fix.style.display="none";
    }


 }
 function onMouseover(){
    fix.style.backgroundColor="red";
 }
 function onMouseOut(){
    fix.style.backgroundColor="aquamarine";
 }
